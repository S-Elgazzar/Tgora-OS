--     does not introduce an isolated policy for just these columns
--   - backfill any existing finance_forecasts row — every new column is
--     nullable (or defaults to false) and every existing row simply gets
--     the "not generated from schedule" values below
--
-- Investigation findings this design is based on (repository inspection,
-- not assumption):
--   - finance_forecasts.id is bigserial; project_payment_schedule_items.id
--     is `bigint GENERATED BY DEFAULT AS IDENTITY` — both resolve to
--     bigint, so a bigint FK column is type-compatible with no cast.
--   - finance_forecasts.is_deleted and .status are both NOT NULL with
--     defaults (is_deleted defaults false, status defaults 'expected')
--     — confirmed in finance_migration_4_1.sql / finance_migration_4_1a.sql
--     — so a duplicate-prevention predicate never has to special-case a
--     NULL is_deleted or NULL status.
--   - finance_forecasts.status values in actual use (UI select +
--     FINANCE_FORECAST_STATUS_LABELS in app.js): expected, committed,
--     received, cancelled (there is no DB CHECK constraint on this
--     column today — enforcement is JS-layer only, unchanged by this
--     migration).
--   - archiveFinanceForecast()/restoreFinanceForecast() in app.js treat
--     is_archived as an independent visibility toggle, not a business-
--     state change: an archived forecast is still real, still restorable,
--     and is never implicitly treated as invalid/superseded elsewhere in
--     the codebase (same pattern as CRM contacts/deals/leads archive).
--     Per the approved decision rule, archive is therefore NOT treated
--     as "non-active" for duplicate prevention below — only is_deleted
--     and status = 'cancelled' do that.
--   - finance_forecasts.created_by / finance_transactions.deleted_by /
--     archived_by are all `uuid NULL REFERENCES auth.users(id)` — the
--     Finance-module convention for actor columns. generated_by below
--     follows that same convention (project_commercial_terms/
--     project_payment_schedule_items.created_by, by contrast, is a bare
--     `uuid NULL` with no FK — that is the Project-module convention;
--     this migration follows the Finance-module convention since the
--     column lives on finance_forecasts).
--   - finance_forecasts.project_ref_id is `bigint NULL`, added in
--     finance_migration_4_1.sql, with NO FK constraint. A repository-wide
--     search found zero reads or writes of this column anywhere in
--     app.js or index.html — it has never been populated by any
--     application code path. However, this migration has no access to
--     the live Supabase database, so it cannot confirm every existing
--     row's project_ref_id is NULL or points at a valid projects.id (a
--     value could in principle have been written by hand outside the
--     app). Per the approved decision rule ("do NOT add the FK if live-
--     data validity cannot be proven from repository inspection"), the
--     FK is deferred — see verification query 6 below, which must be
--     run and confirmed empty before any future hardening migration
--     adds `REFERENCES projects(id)` to this column.
