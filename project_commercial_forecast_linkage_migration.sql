--     in any way
--   - touch finance_transactions in any way (no payment_schedule_item_id
--     or forecast_id column added there — that is future, unapproved
--     scope per the Project Commercial C report's "Actual Layer")
--   - modify convertForecastToTransaction() or any other app.js logic
--   - add a real FK from finance_forecasts.project_ref_id to projects(id)
--     — investigated and deliberately deferred, see "Project Linkage
--     Decision" below
--   - add RLS policies — no table in this codebase has a verified,
--     working RLS policy to model from (confirmed again in this sprint's
