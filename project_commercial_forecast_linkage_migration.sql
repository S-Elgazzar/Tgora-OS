-- 1. Extend finance_forecasts with schedule-linkage columns.
ALTER TABLE finance_forecasts
  ADD COLUMN IF NOT EXISTS payment_schedule_item_id  bigint NULL REFERENCES project_payment_schedule_items(id),
  ADD COLUMN IF NOT EXISTS forecast_component        text NULL,
  ADD COLUMN IF NOT EXISTS generated_from_schedule    boolean NOT NULL DEFAULT false,
  ADD COLUMN IF NOT EXISTS source_snapshot_amount     numeric(15,2) NULL,
  ADD COLUMN IF NOT EXISTS source_snapshot_due_date   date NULL,
  ADD COLUMN IF NOT EXISTS source_snapshot_updated_at timestamptz NULL,
  ADD COLUMN IF NOT EXISTS generated_by               uuid NULL REFERENCES auth.users(id);

-- 2. forecast_component enum-style guard. NULL remains allowed (every
--    manually-created forecast, past and future, has no component).
--    No NOT VALID needed here — unlike crm_leads_status_check (Sprint
--    CRM-4.5D Fix Pass), this is a brand-new column being added in this
--    same migration, so every existing row is guaranteed to receive NULL
--    for it and trivially satisfies the constraint; there is no unknown
--    live-data risk to guard against.
ALTER TABLE finance_forecasts DROP CONSTRAINT IF EXISTS finance_forecasts_component_check;
ALTER TABLE finance_forecasts ADD CONSTRAINT finance_forecasts_component_check
  CHECK (forecast_component IS NULL OR forecast_component IN ('revenue', 'client_funds'));

-- 3. Lookup index — "does this Payment Item have any generated forecasts".
CREATE INDEX IF NOT EXISTS finance_forecasts_payment_schedule_item_idx
  ON finance_forecasts(payment_schedule_item_id);

-- 4. Duplicate-prevention: at most one active generated forecast per
--    (payment_schedule_item_id, forecast_component). Scoped to
--    generated_from_schedule = true so it never constrains manually
--    created forecasts (which always have payment_schedule_item_id
--    NULL in this sprint, but the predicate is explicit rather than
--    relying on that alone). Excludes is_deleted and status = 'cancelled'
--    rows so a cancelled/deleted generated forecast can be regenerated —
--    per the investigation above, is_archived is deliberately NOT part
--    of this predicate, since archive does not mean "non-active" for any
--    other Finance record in this codebase.
CREATE UNIQUE INDEX IF NOT EXISTS finance_forecasts_schedule_component_unique_idx
  ON finance_forecasts (payment_schedule_item_id, forecast_component)
  WHERE generated_from_schedule = true
    AND is_deleted = false
    AND status <> 'cancelled';

-- Force PostgREST to reload its schema cache immediately.
NOTIFY pgrst, 'reload schema';

